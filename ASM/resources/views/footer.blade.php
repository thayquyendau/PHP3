<!-- JavaScript for Dynamic Year, Form Validation, Language Dropdown, and Back to Top -->
<script>
    // Set the current year dynamically
    document.getElementById('current-year').textContent = new Date().getFullYear();

    // Newsletter form validation
    (function () {
        'use strict';
        const form = document.getElementById('newsletter-form');
        form.addEventListener('submit', function (event) {
            if (!form.checkValidity()) {
                event.preventDefault();
                event.stopPropagation();
            }
            form.classList.add('was-validated');
        }, false);
    })();

    // Language dropdown hover behavior
    const languageDropdown = document.getElementById('languageDropdown');
    const languageDropdownMenu = document.getElementById('languageDropdownMenu');
    languageDropdown.addEventListener('mouseenter', () => {
        languageDropdownMenu.style.display = 'block';
    });
    languageDropdown.parentElement.addEventListener('mouseleave', () => {
        languageDropdownMenu.style.display = 'none';
    });

    // Smooth scroll for Back to Top button
    document.getElementById('toTop').addEventListener('click', (e) => {
        e.preventDefault();
        window.scrollTo({ top: 0, behavior: 'smooth' });
    });

    // Show/Hide Back to Top button on scroll
    window.addEventListener('scroll', () => {
        const toTopButton = document.getElementById('toTop');
        if (window.scrollY > 300) {
            toTopButton.classList.remove('d-none');
        } else {
            toTopButton.classList.add('d-none');
        }
    });
</script>

